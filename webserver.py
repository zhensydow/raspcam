#!/usr/bin/env python
""" Raspberry Camera Web Server main file.
"""
#------------------------------------------------------------------------------
import web

#------------------------------------------------------------------------------
URLS = (
    '/', 'Main'
)


#------------------------------------------------------------------------------
class Main(object):
    """Class to Handle root urls"""
    def GET(self):
        return 'Hello, World!'


#------------------------------------------------------------------------------
def main():
    app = web.application(URLS, globals())
    app.run()


#------------------------------------------------------------------------------
if __name__ == "__main__":
    main()

